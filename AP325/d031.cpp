#define MAXN 200005
#define MAXM 1000005 
int n, m;
int x[MAXN];

void sol()
{
	cin >> n >> m;
	for (int i = 0; i < n; i++) {
		cin >> x[i];
	}
	int l = 0, r = 0;
	pii ans = {0, 0};
	int sum = 0;
	for (r = 0; r < n; r++) {
		sum += x[r];
		while (sum > m && l < r) {
			sum -= x[l];
			l++;
		}
		if (sum <= m && sum > ans.F) {
			ans = {sum, 1};
		}
		else if (sum == ans.F)
			ans.S++;
	}
	cout << ans.F << endl << ans.S << endl;
}


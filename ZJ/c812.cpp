int n, v, m;
vector<pii> G[MAXN];
bitset<MAXN> vis;

int dfs(int i) {
    vis[i] = true;
    int res = 0;

    for (auto e: G[i]) if (!vis[e.F]) {
        res += (e.S >= m) * (dfs(e.F) + 1);
    }
    return res;
}

